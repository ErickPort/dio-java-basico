import java.util.Scanner;

public class Soma {
     public static void main(String[] args) {
        Scanner scanner = new Scanner(System.in);   

        System.out.print("Digite a quantidade de termos que deve ter a soma: ");
        int soma = scanner.nextInt(); 
        for (int i = 1; i <= soma; i++) {
            for (int j = 1; j <= i; j++) {
                System.out.print( );
            }
            System.out.println();
        }   


    }
}
    

